CREATE TABLE APP.APP_USER(
    ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1000, INCREMENT BY 1),
    TYPE INTEGER NOT NULL,
    NAME VARCHAR(40) NOT NULL,
    PASSWORD VARCHAR(40) NOT NULL,
    PRIMARY KEY(ID)
);

CREATE TABLE APP.EVENT(
    ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1000, INCREMENT BY 1),
    TYPE INTEGER NOT NULL,
    NAME VARCHAR(120) NOT NULL,
    OWNER_ID BIGINT NOT NULL REFERENCES APP.APP_USER(ID),
    PRIMARY KEY(ID)
);

CREATE TABLE APP.ACTIVITY(
    ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1000, INCREMENT BY 1),
    TYPE INTEGER NOT NULL,
    NAME VARCHAR(120) NOT NULL,
    EVENT_ID BIGINT NOT NULL REFERENCES APP.EVENT(ID),
    DEMAND INTEGER NOT NULL DEFAULT 0,
    CAPACITY INTEGER NOT NULL DEFAULT 0,
    SALARY INTEGER NOT NULL DEFAULT 0,
    PRIMARY KEY(ID)
);

CREATE TABLE APP.VOLUNTEER(
    ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1000, INCREMENT BY 1),
    USER_ID BIGINT NOT NULL REFERENCES APP.APP_USER(ID),
    ACTIVITY_ID BIGINT NOT NULL REFERENCES APP.ACTIVITY(ID),
    PRIMARY KEY(ID)
);